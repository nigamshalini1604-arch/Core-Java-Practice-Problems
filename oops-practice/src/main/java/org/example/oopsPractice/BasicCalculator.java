package org.example.oopsPractice;

public class BasicCalculator {


    public int add(int a, int b){
        return a+b;
    }
    public int sub(int a, int b){
        return a-b;
    }
    public int multiply(int a, int b){
        return a*b;
    }
    public int devide(int a, int b){
        return a/b;
    }
}
